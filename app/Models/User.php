class User extends Authenticatable
{
    use HasApiTokens;
    use HasFactory;
    use Notifiable;

    protected $table = 'users';

    protected $with = ['roles'];
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
     */
//    protected $appends = [
//        'profile_photo_url',
//    ];

    public function isAdmin(): bool
    {
        return $this->hasRoleWithId(RoleEnum::ADMIN);
    }

    public function hasRoleWithId(int $roleId): bool
    {
        $rolesIds = $this->roles->pluck('id')->toArray();
        return in_array($roleId, $rolesIds);
    }

    public function isNinja(): bool
    {
        return $this->hasRoleWithId(RoleEnum::NINJA);
    }

    public function isManager(): bool
    {
        return $this->hasRoleWithId(RoleEnum::MANAGER);
    }

    public function isCutter(): bool
    {
        return $this->hasRoleWithId(RoleEnum::CUTTER);
    }

    public function isExternal(): bool
    {
        return $this->hasRoleWithId(RoleEnum::EXTERNAL_USER);
    }

    public function plans()
    {
        return $this->hasMany(Plan::class);
    }

    public function roles(): BelongsToMany
    {
        return $this->belongsToMany(Role::class, 'user_role', 'user_id', 'role_id');
    }

    public function hasAccess(array $permissions): bool
    {
        // check if the permission is available in any role
        foreach ($this->roles as $role) {
            if ($role->hasAccess($permissions)) {
                return true;
            }
        }
        return false;
    }
}
